    //For getting your desired end time
    public String getSelectedTime() {
        String hour = jcboxHour.getSelectedItem().toString();
        String minute = jcboxMinutes.getSelectedItem().toString();
        String period = jcboxAMPM.getSelectedItem().toString();
        return hour + ":" + minute + " " + period;
    }

    //For checking tasks during add and update tasks
    public boolean checkTasks(boolean isUpdate, int selectedRow) {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        String normalizedNewName = jtxtTaskName.getText().replaceAll("\\s+", "");
        String selectedDate = formatter.format(jdDate.getDate());
        String selectedTime = getSelectedTime();

        for (int i = 0; i < tableModel.getRowCount(); i++) {
            if (isUpdate && i == selectedRow) {
                continue;
            }

            String existingTaskName = tableModel.getValueAt(i, 0).toString().replaceAll("\\s+", "");
            String existingDate = tableModel.getValueAt(i, 1).toString();
            String existingTime = tableModel.getValueAt(i, 2).toString();

            // Conflict: same name and date, or same time
            boolean sameNameAndDate = normalizedNewName.equalsIgnoreCase(existingTaskName) && selectedDate.equals(existingDate);
            boolean sameTime = selectedTime.equals(existingTime);

            if (sameNameAndDate && sameTime) {
                return true; // Conflict found
            }
        }
        return false; // No conflict
    }

    private void jbtnAddTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddTaskActionPerformed
        try {
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            String fullDateTimeStr = formatter.format(jdDate.getDate()) + " " + getSelectedTime();
            SimpleDateFormat fullFormatter = new SimpleDateFormat("MMMM dd, yyyy hh:mm a");
            Date selectedDateTime = null;
            
            if (jtxtTaskName.getText().equals("") || jcboxHour.getSelectedItem().toString().equals("00")) {
                JOptionPane.showMessageDialog(null, "Please enter the required field!");
            } else {
                try {
                    selectedDateTime = fullFormatter.parse(fullDateTimeStr);
                } catch (ParseException e) {
                    JOptionPane.showMessageDialog(null, "Invalid date or time.");
                    return;
                }

                Date now = new Date();
                if (selectedDateTime.before(now)) {
                    JOptionPane.showMessageDialog(null, "The selected date and time has already passed!");
                } else if (checkTasks(false, -1)) {
                    JOptionPane.showMessageDialog(null, "This task is already exists!");
                } else {
                    String[] data = {jtxtTaskName.getText(), formatter.format(jdDate.getDate()), getSelectedTime()};

                    JOptionPane.showMessageDialog(null, "Task added successfully!");
                    tableModel.addRow(data);

                    try (Connection conn = Database.connect()) {
                        String sql = "INSERT INTO tasks(name, endDate, endTime) VALUES(?, ?, ?)";
                        PreparedStatement pstmt = conn.prepareStatement(sql);
                        pstmt.setString(1, jtxtTaskName.getText());
                        pstmt.setString(2, formatter.format(jdDate.getDate()));
                        pstmt.setString(3, getSelectedTime());
                        pstmt.executeUpdate();
                    } catch (SQLException ex) {
                        ex.printStackTrace();
                    }

                    generalAfterSet();
                }
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Please enter the required field!");
        }
        
    }//GEN-LAST:event_jbtnAddTaskActionPerformed

    private void jbtnDeleteTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteTaskActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        if (JOptionPane.showConfirmDialog(null, "Would you delete this task?", "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if (jTable1.getSelectedRowCount() == 1) {
                String taskName = jtxtTaskName.getText();
                JOptionPane.showMessageDialog(null, "Task delete successfully!");
                tableModel.removeRow(jTable1.getSelectedRow());

                try (Connection conn = Database.connect()) {
                    String sql = "DELETE FROM tasks WHERE name = ?";
                    PreparedStatement pstmt = conn.prepareStatement(sql);
                    pstmt.setString(1, taskName);
                    pstmt.executeUpdate();
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage()); // or show message
                }

                generalAfterSet();
            } else {
                if (jTable1.getModel().getRowCount() == 0) {
                    JOptionPane.showMessageDialog(null, "Table is empty.");
                } else {
                    JOptionPane.showMessageDialog(null, "Please select a single row.");
                }
            }
        }
    }//GEN-LAST:event_jbtnDeleteTaskActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        String tblName = tableModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String tblEndDate = tableModel.getValueAt(jTable1.getSelectedRow(), 1).toString();
        jtxtTaskName.setText(tblName);

        try {
            Date date = formatter.parse(tblEndDate);
            jdDate.setDate(date);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        String tblEndTime = tableModel.getValueAt(jTable1.getSelectedRow(), 2).toString().replaceAll("[: ]", "/");
        String splitSectionTime[] = tblEndTime.split("/");
        jcboxHour.setSelectedItem(splitSectionTime[0]);
        jcboxMinutes.setSelectedItem(splitSectionTime[1]);
        jcboxAMPM.setSelectedItem(splitSectionTime[2]);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jbtnUpdateTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateTaskActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        if (JOptionPane.showConfirmDialog(null, "Would you update this task?", "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if (jtxtTaskName.getText().equals("") || jdDate.getDate() == null || jcboxHour.getSelectedItem().toString().equals("00")) {
                JOptionPane.showMessageDialog(null, "Please enter the required field!");
            }
            else {
                if (jTable1.getSelectedRowCount() == 1) {
                    String originalName = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
                    String name = jtxtTaskName.getText();
                    String date = formatter.format(jdDate.getDate());

                    if (checkTasks(true, jTable1.getSelectedRow())) {
                        JOptionPane.showMessageDialog(null, "This task is still the same!");
                    } else {
                        //set updated value on table row
                        tableModel.setValueAt(name, jTable1.getSelectedRow(), 0);
                        tableModel.setValueAt(date, jTable1.getSelectedRow(), 1);
                        tableModel.setValueAt(getSelectedTime(), jTable1.getSelectedRow(), 2);

                        try (Connection conn = Database.connect()) {
                            String sql = "UPDATE tasks SET name = ?, endDate = ?, endTime = ? WHERE name = ?";
                            PreparedStatement pstmt = conn.prepareStatement(sql);
                            pstmt.setString(1, name);
                            pstmt.setString(2, date);
                            pstmt.setString(3, getSelectedTime());
                            pstmt.setString(4, originalName);
                            pstmt.executeUpdate();
                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(null, e.getMessage()); // or show message
                        }

                        JOptionPane.showMessageDialog(null, "Task updated successfully!");
                        generalAfterSet();
                    }
                } else {
                    if (jTable1.getModel().getRowCount() == 0) {
                        JOptionPane.showMessageDialog(null, "Table is empty.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Please select a single row.");
                    }
                }
            }
        }
    }//GEN-LAST:event_jbtnUpdateTaskActionPerformed

