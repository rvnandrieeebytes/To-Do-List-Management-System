    //Load Tasks from Database if there's data after exit
    private void loadTasksFromDatabase() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        try (Connection conn = Database.connect(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery("SELECT name, endDate, endTime FROM tasks")) {
            while (rs.next()) {
                tableModel.addRow(new Object[]{
                    rs.getString("name"),
                    rs.getString("endDate"),
                    rs.getString("endTime")
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    //Autoremove Expired Tasks
    private void autoRemoveExpiredTasks() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        Date now = new Date(); // current date and time

        for (int i = tableModel.getRowCount() - 1; i >= 0; i--) {
            String dateStr = tableModel.getValueAt(i, 1).toString(); // "MMMM dd, yyyy"
            String timeStr = tableModel.getValueAt(i, 2).toString(); // "hh:mm AM/PM"
            try {
                // Combine date and time into a full datetime string
                String datetimeStr = dateStr + " " + timeStr;
                SimpleDateFormat fullFormatter = new SimpleDateFormat("MMMM dd, yyyy hh:mm a");

                Date taskEnd = fullFormatter.parse(datetimeStr);

                // If the current time is after or equal to the task end time
                if (now.compareTo(taskEnd) >= 0) {
                    String taskName = tableModel.getValueAt(i, 0).toString();
                    tableModel.removeRow(i); // Remove from UI

                    // Remove from database
                    try (Connection conn = Database.connect()) {
                        String sql = "DELETE FROM tasks WHERE name = ?";
                        PreparedStatement pstmt = conn.prepareStatement(sql);
                        pstmt.setString(1, taskName);
                        pstmt.executeUpdate();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Failed to delete expired task from DB: " + e.getMessage());
                    }
                }
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Failed to parse task date/time: " + e.getMessage());
            }
        }
    }

    //After adding, updating, and deleting tasks, autoempty the textboxes
